
public class Bomba {
	private int presFinal;
	private int presInic;
	
	public int resultado() 
	{
		return this.presFinal - this.presInic;
	}

	public int getPresFinal() {
		return presFinal;
	}

	public void setPresFinal(int presFinal) {
		this.presFinal = presFinal;
	}

	public int getPresInic() {
		return presInic;
	}

	public void setPresInic(int presInic) {
		this.presInic = presInic;
	}

	
	
	
}
